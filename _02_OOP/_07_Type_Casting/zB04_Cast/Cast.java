public class Cast {
    
    public static void main(String[] args) {
        
        double a = 10.55;
        
        int b = (int)a;
        
        double c = b;
        
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        
    }
    
}
